package DAY01;

public class typecastingEx1 {

	// 자동 자료형 변환 예제
	// 작은 자료형 값을 큰 자료형으로 변환
	// 정수를 실수로 변환
	public static void main(String[] args) {
		
		double num1 = 1; // 리터럴 1을 int 에서 double 로 자동형변환 저장
		System.out.println(num1);
		
		long num2 = 2;// 리터럴 2을 int 에서 long 로 자동형변환 저장
		System.out.println(num2);
		
		char ch = 'a';
		int num3 = ch; // 'a' 의 아스키코드값인 97이 num3 에 저장
		System.out.println(num3);
		
		
	
		
		
		
		

	}

}
